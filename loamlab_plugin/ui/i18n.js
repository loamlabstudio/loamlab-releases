// ── DEV 驗證：只在開發版執行，列出缺漏 key ──────────────────────────
// 在 SketchUp Ruby Console 觀察輸出即可確認所有語言同步
if (typeof window !== 'undefined' && document.title && document.title.includes('[DEV]')) {
    const _baseKeys = Object.keys(UI_LANG['en-US']);
    Object.keys(UI_LANG).forEach(lang => {
        const missing = _baseKeys.filter(k => !(k in UI_LANG[lang]));
        if (missing.length) console.warn(`[i18n] ${lang} missing (${missing.length}): ${missing.join(', ')}`);
    });
}

let currentLang = 'en-US';

window.setLanguage = function (lang) {
    if (!UI_LANG[lang]) return;
    currentLang = lang;

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (UI_LANG[lang][key]) {
            if (key === 'sync_screen') {
                el.innerHTML = `<span class="w-1.5 h-1.5 rounded-full bg-[#dc2626] animate-pulse"></span> ${UI_LANG[lang][key]}`;
            } else if (el.tagName.toLowerCase() === 'textarea' || el.tagName.toLowerCase() === 'input') {
                el.placeholder = UI_LANG[lang][key];
            } else {
                el.textContent = UI_LANG[lang][key];
            }
        }
    });

    const scenesSpan = document.getElementById('scene-count-label');
    if (scenesSpan) {
        const count = scenesSpan.getAttribute('data-count') || 0;
        scenesSpan.textContent = `${UI_LANG[lang]['total']} ${count} ${UI_LANG[lang]['unit']}`;
    }

    if (typeof updatePlanCostLabels === 'function') updatePlanCostLabels(lang);
    if (typeof setActiveTool === 'function' && typeof currentActiveTool !== 'undefined') setActiveTool(currentActiveTool);
};
